
public class Zi {
	private String ziDinSaptamana;
	private boolean ziLucratoare;

	public Zi(String ziDinSaptamana, boolean ziLucratoare) {
		this.ziDinSaptamana = ziDinSaptamana;
		this.ziLucratoare = ziLucratoare;
	}

	public String getZiDinSaptamana() {
		return ziDinSaptamana;
	}

	public void setZiDinSaptamana(String ziDinSaptamana) {
		this.ziDinSaptamana = ziDinSaptamana;
	}

	public boolean isZiLucratoare() {
		return ziLucratoare;
	}

	public void setZiLucratoare(boolean ziLucratoare) {
		this.ziLucratoare = ziLucratoare;
	}

}
